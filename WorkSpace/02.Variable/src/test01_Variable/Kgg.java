package test01_Variable;

public class Kgg {public static void main(String[] args) {
	int a;
	a=10;
	System.out.println(a);
	float b;
	b=1.23F;
	System.out.println(b);
	long c;
	c=123123123123L;
	System.out.println(c);
	byte d;
	d=20;
	System.out.println(d);
	double e;
	e= 2.34;
	System.out.println(e);
	
	//1.숫자 - int(정수) < long(정수)
	//       float(실수) < double(실수)
	// 변수 규칙:
	// 1. 선언: 변수타입 변수명 ;
	// 2. 할당: 변수명 = 변수타입에 담을수있는 값;
	// 3. 초기화: ↑두가지를 한번에 하는것
	//변수타입 변수명 = 값 ;
    int iNum;
    iNum=10;
    long lNum;
    lNum=10L;
    float fNum=3.14F;
    double dNum=3.14;
    
    
    System.out.println(iNum);
    System.out.println(lNum);
	System.out.println(fNum);
	System.out.println(dNum);
	
	//initialized(초기화)
	
	
	
	
	
	
}

}
